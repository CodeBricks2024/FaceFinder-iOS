import UIKit

extension UIColor {
    
    static var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    static var primaryColor: UIColor {
        return UIColor(red: 0 / 255.0, green: 71 / 255.0, blue: 187.0 / 255.0, alpha: 1.0)
    }
}
